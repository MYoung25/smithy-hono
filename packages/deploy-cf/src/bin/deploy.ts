/** Log a progress/step line to stderr (stdout is reserved for machine output). */
function log(msg: string): void {
  process.stderr.write(msg + '\n')
}

/** Print an error to stderr and exit with the given non-zero code. */
function fail(msg: string, code = 1): never {
  process.stderr.write('error: ' + msg + '\n')
  process.exit(code)
}

/** Await-based sleep used between health-probe attempts. */
function sleep(ms: number): Promise<void> {
  return new Promise((r) => setTimeout(r, ms))
}

const USAGE = `smithy-hono-deploy — one-command Cloudflare Workers deploy

Usage:
  smithy-hono-deploy <domain> [--rotate-keys] [--skip-build] [--config <path>]

Arguments:
  <domain>          Bare hostname to deploy to (e.g. app.example.com). No scheme,
                    path, whitespace or trailing dot.

Flags:
  --rotate-keys     Force regeneration of all generated secrets (re-put them).
  --skip-build      Skip the assets build step.
  --overwrite       Allow overwriting a hand-authored wrangler.toml (one not
                    generated by this tool). Refused by default to avoid clobbering.
  --config <path>   Path to the deploy config (overrides auto-discovery).

Environment:
  CLOUDFLARE_ACCOUNT_ID   Short-circuit / override account detection.
  CLOUDFLARE_API_TOKEN    If set, enables a best-effort zone-active precheck.`

// ---------------------------------------------------------------------------
// CLI args
// ---------------------------------------------------------------------------

interface CliArgs {
  domain: string
  rotateKeys: boolean
  skipBuild: boolean
  overwrite: boolean
  configPath: string | undefined
}

/**
 * Parse + validate argv. The domain is positional; flags may appear anywhere.
 * Rejects empty / scheme-bearing / path-bearing / whitespace / trailing-dot
 * domains. Prints usage + exits 1 on any bad input.
 */
function parseArgs(argv: string[]): CliArgs {
  let domain: string | undefined
  let rotateKeys = false
  let skipBuild = false
  let overwrite = false
  let configPath: string | undefined

  for (let i = 0; i < argv.length; i++) {
    const tok = argv[i]
    if (tok === '--rotate-keys') {
      rotateKeys = true
    } else if (tok === '--skip-build') {
      skipBuild = true
    } else if (tok === '--overwrite') {
      overwrite = true
    } else if (tok === '--config') {
      configPath = argv[i + 1]
      i++
    } else if (tok === '-h' || tok === '--help') {
      process.stderr.write(USAGE + '\n')
      process.exit(1)
    } else if (tok.startsWith('--')) {
      process.stderr.write(USAGE + '\n')
      fail(`unknown flag: ${tok}`)
    } else if (domain === undefined) {
      domain = tok
    }
  }

  if (configPath !== undefined && configPath.length === 0) {
    process.stderr.write(USAGE + '\n')
    fail('--config requires a path argument')
  }

  if (!domain) {
    process.stderr.write(USAGE + '\n')
    fail('a <domain> argument is required')
  }
  validateDomain(domain)

  return { domain, rotateKeys, skipBuild, overwrite, configPath }
}

/**
 * Strict DNS-hostname grammar: dot-separated labels, each 1–63 chars of
 * `[a-z0-9-]`, no leading/trailing hyphen, total ≤ 253 chars. Rejects `_`,
 * consecutive dots, scheme/path/whitespace and trailing dots. Case-insensitive.
 *
 * This is intentionally stricter than "no scheme/path/whitespace": distinct forms
 * like `a_b.example.com` vs `a-b.example.com` must NOT be accepted, because the
 * state-file slug + default resource titles would otherwise alias them onto one
 * another (DEPLOY-CF-PKG-3).
 */
const HOSTNAME_RE =
  /^(?=.{1,253}$)([a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?)(\.([a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?))*$/i

/** True iff `domain` is a syntactically valid bare DNS hostname (see HOSTNAME_RE). */
export function isValidDomain(domain: string): boolean {
  return HOSTNAME_RE.test(domain)
}

/** Reject anything that is not a bare hostname. Exits 1 (with usage) on failure. */
function validateDomain(domain: string): void {
  if (!isValidDomain(domain)) {
    process.stderr.write(USAGE + '\n')
    fail(
      `invalid domain "${domain}" — pass a bare hostname like app.example.com ` +
        `(dot-separated labels of a–z, 0–9 and '-', no scheme, path, underscore, ` +
        `whitespace, leading/trailing hyphen or trailing dot).`,
    )
  }
}

