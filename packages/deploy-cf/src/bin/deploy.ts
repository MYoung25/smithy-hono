/** A Cloudflare account id is exactly 32 hex chars. Anchored: the WHOLE env value. */
const HEX32_ANCHORED = /^[0-9a-f]{32}$/i
/** Global variant for scanning `wrangler whoami` output (may list several ids). */
const HEX32_GLOBAL = /[0-9a-f]{32}/gi

/**
 * Pure resolver for the account id (no I/O — unit-tested). Precedence:
 *  1. `CLOUDFLARE_ACCOUNT_ID` if set: it MUST be exactly 32 hex, else it's an error
 *     (never trust a malformed override — DEPLOY-CF-PKG-1). No silent fallback.
 *  2. Otherwise scan `wrangler whoami` output for 32-hex ids and DEDUPE:
 *     - exactly one distinct id → use it;
 *     - more than one (multi-account user) → error asking for CLOUDFLARE_ACCOUNT_ID
 *       instead of guessing whichever wrangler happened to list first;
 *     - none → error.
 */
export function resolveAccountId(
  envId: string | undefined,
  whoamiOutput: string,
): { id: string } | { error: string } {
  if (envId !== undefined && envId.length > 0) {
    if (HEX32_ANCHORED.test(envId)) return { id: envId.toLowerCase() }
    return {
      error:
        `CLOUDFLARE_ACCOUNT_ID is set but is not a valid 32-hex account id ("${envId}"). ` +
        `Unset it or set it to your account id (32 hex chars).`,
    }
  }
  const ids = Array.from(
    new Set((whoamiOutput.match(HEX32_GLOBAL) ?? []).map((s) => s.toLowerCase())),
  )
  if (ids.length === 1) return { id: ids[0] }
  if (ids.length > 1) {
    return {
      error:
        `multiple Cloudflare accounts found in \`wrangler whoami\` — refusing to guess. ` +
        `Set CLOUDFLARE_ACCOUNT_ID to the account you want to deploy to.`,
    }
  }
  return {
    error:
      'could not determine the Cloudflare account — run `wrangler login` or set CLOUDFLARE_ACCOUNT_ID.',
  }
}

/**
 * Ensure wrangler is runnable and resolve the Cloudflare account id via
 * {@link resolveAccountId}. Exits 1 with actionable guidance if wrangler is missing
 * or no unambiguous account id can be determined.
 */
function ensureAccount(cwd: string): string {
  const version = runWrangler(['--version'], { cwd })
  if (version.status !== 0) {
    fail("wrangler not found — install it (npm i -D wrangler) or ensure it's on PATH.")
  }
  log(`wrangler: ${combined(version).trim().split('\n')[0] || 'ok'}`)

  const envId = process.env.CLOUDFLARE_ACCOUNT_ID
  if (envId && HEX32_ANCHORED.test(envId)) {
    const resolved = resolveAccountId(envId, '')
    if ('error' in resolved) fail(resolved.error)
    log(`account id: from CLOUDFLARE_ACCOUNT_ID`)
    return resolved.id
  }
  // If the env override is set-but-malformed, resolveAccountId reports the error
  // WITHOUT falling back to `wrangler whoami` (an invalid override is a hard stop).
  if (envId !== undefined && envId.length > 0) {
    const bad = resolveAccountId(envId, '')
    if ('error' in bad) fail(bad.error)
  }

  const who = runWrangler(['whoami'], { cwd })
  const resolved = resolveAccountId(undefined, combined(who))
  if ('error' in resolved) fail(resolved.error)
  log(`account id: resolved from \`wrangler whoami\``)
  return resolved.id
}

// ---------------------------------------------------------------------------
// Zone precheck (best-effort)
// ---------------------------------------------------------------------------

/**
 * Generate registrable-suffix candidates for a domain by dropping leftmost
 * labels, stopping at 2 labels (never a bare TLD). e.g. `a.b.example.com` →
 * `[a.b.example.com, b.example.com, example.com]`.
 */
function zoneCandidates(domain: string): string[] {
  const labels = domain.split('.')
  const out: string[] = []
  for (let i = 0; i + 2 <= labels.length; i++) {
    out.push(labels.slice(i).join('.'))
  }
  return out
}

/**
 * Best-effort check that the domain's zone is active on the account. Only runs
 * when `CLOUDFLARE_API_TOKEN` is set; otherwise warns + continues (wrangler
 * deploy will surface a misconfigured zone). Exits 1 with actionable guidance
 * when a token is present but no candidate zone is active.
 */
async function checkZone(domain: string): Promise<void> {
  const token = process.env.CLOUDFLARE_API_TOKEN
  if (!token) {
    log('warning: CLOUDFLARE_API_TOKEN not set — skipping zone precheck (wrangler deploy will verify).')
    return
  }
  for (const candidate of zoneCandidates(domain)) {
    const url = `https://api.cloudflare.com/client/v4/zones?status=active&name=${encodeURIComponent(candidate)}`
    try {
      const resp = await fetch(url, { headers: { Authorization: `Bearer ${token}` } })
      const body = (await resp.json()) as { result?: unknown[] }
      if (Array.isArray(body.result) && body.result.length > 0) {
        log(`zone active: ${candidate}`)
        return
      }
    } catch {
      // Network errors shouldn't block a deploy — fall through to the warning.
      log('warning: zone precheck request failed — continuing (wrangler deploy will verify).')
      return
    }
  }
  fail(
    `no active Cloudflare zone found for "${domain}" on this account. ` +
      `Add the domain in the Cloudflare dashboard and delegate your registrar's ` +
      `nameservers to Cloudflare, wait for it to go active, then re-run.`,
  )
}

// ---------------------------------------------------------------------------
// State file
// ---------------------------------------------------------------------------

interface DeployState {
  kvIds: Record<string, string>
  d1Ids: Record<string, string>
  secretsProvisioned: Record<string, boolean>
}

/**
 * Slugify a domain for filesystem-safe state-file naming. A readable slug PLUS a
 * short hash of the exact lowercased domain, so distinct hostnames that would
 * otherwise collapse to the same readable slug (e.g. `a.b.example.com`,
 * `a-b.example.com`) get DISTINCT state files and never cross-wire each other's
 * KV/D1 ids or secrets (DEPLOY-CF-PKG-3). validateDomain already rejects `_`, so
 * the collision surface is narrowed further at the input boundary.
 *
 * NOTE: this changes the slug vs. the previous scheme, orphaning any existing
 * `.smithy-deploy/<oldslug>.json` — a re-run simply re-provisions (idempotent via
 * resource adoption). Not a public API.
 */
export function domainSlug(domain: string): string {
  const lower = domain.toLowerCase()
  const readable = lower.replace(/[^a-z0-9]+/g, '-').replace(/^-+|-+$/g, '')
  const hash = createHash('sha256').update(lower).digest('hex').slice(0, 8)
  return `${readable}-${hash}`
}

/** Load persisted deploy state for this domain (defaults to empty), ensuring the dir exists. */
function loadState(configDir: string, slug: string): { state: DeployState; path: string } {
  const dir = resolve(configDir, '.smithy-deploy')
  mkdirSync(dir, { recursive: true })
  const path = resolve(dir, `${slug}.json`)
  let state: DeployState = { kvIds: {}, d1Ids: {}, secretsProvisioned: {} }
  if (existsSync(path)) {
    const parsed = JSON.parse(readFileSync(path, 'utf8')) as Partial<DeployState>
    state = {
      kvIds: parsed.kvIds ?? {},
      d1Ids: parsed.d1Ids ?? {},
      secretsProvisioned: parsed.secretsProvisioned ?? {},
    }
  }
  return { state, path }
}

/** Persist deploy state (called after each successful provisioning step). */
function saveState(path: string, state: DeployState): void {
  writeFileSync(path, JSON.stringify(state, null, 2) + '\n')
}

// ---------------------------------------------------------------------------
// Provisioning
// ---------------------------------------------------------------------------

const KV_ID = /id\s*=\s*"([0-9a-f]{32})"|"id":\s*"([0-9a-f]{32})"/i
const D1_ID =
  /database_id\s*=\s*"([0-9a-f-]{36})"|"database_id":\s*"([0-9a-f-]{36})"/i

/**
 * Extract a JSON array from wrangler's STDOUT (pass stdout only — never the
 * stderr-mixed stream). wrangler may still prefix stdout with an npm notice or
 * account banner, so we greedy-slice the first `[` … last `]`. Because stderr
 * (warnings/deprecation banners that can contain `]`) is excluded by the caller,
 * this no longer over-runs the payload (DEPLOY-CF-PKG-5). Returns [] if nothing
 * parses — the caller then falls back to creating the resource.
 */
export function parseJsonArray(out: string): Array<Record<string, unknown>> {
  const start = out.indexOf('[')
  const end = out.lastIndexOf(']')
  if (start === -1 || end === -1 || end < start) return []
  try {
    const parsed: unknown = JSON.parse(out.slice(start, end + 1))
    return Array.isArray(parsed) ? (parsed as Array<Record<string, unknown>>) : []
  } catch {
    return []
  }
}

/**
 * Look up an existing KV namespace id by title via `wrangler kv namespace list`.
 * Returns undefined when absent or unparseable. Lets re-runs adopt a namespace
 * created by an earlier (e.g. partially-failed) deploy when the state file was
 * lost — without this, a fresh state would create a duplicate namespace.
 */
function findExistingKvId(title: string, cwd: string): string | undefined {
  const res = runWrangler(['kv', 'namespace', 'list'], { cwd })
  if (res.status !== 0) return undefined
  const match = parseJsonArray(res.stdout).find((ns) => ns.title === title)
  const id = match?.id
  return typeof id === 'string' ? id : undefined
}

/**
 * Look up an existing D1 database id by name via `wrangler d1 list --json`.
 * Returns undefined when absent or unparseable. D1 names are unique per account,
 * so without adoption a re-run after a lost state file would hard-fail on
 * `d1 create` ("already exists").
 */
function findExistingD1Id(databaseName: string, cwd: string): string | undefined {
  const res = runWrangler(['d1', 'list', '--json'], { cwd })
  if (res.status !== 0) return undefined
  const match = parseJsonArray(res.stdout).find((db) => db.name === databaseName)
  const id = match?.uuid
  return typeof id === 'string' ? id : undefined
}

/**
 * Provision KV namespaces idempotently: reuse ids from state, else adopt an
 * existing same-titled namespace, else create one via
 * `wrangler kv namespace create <title>` and parse the new 32-hex id. State is
 * saved after each resolve. Throws if no id can be parsed from a create.
 */
function provisionKv(
  config: DeployConfig,
  state: DeployState,
  statePath: string,
  slug: string,
  cwd: string,
): void {
  for (const kv of config.bindings?.kv ?? []) {
    if (state.kvIds[kv.binding]) {
      log(`kv ${kv.binding}: reuse ${state.kvIds[kv.binding]}`)
      continue
    }
    const title = kv.title ?? `${config.appName}-${kv.binding}-${slug}`

    const existing = findExistingKvId(title, cwd)
    if (existing) {
      state.kvIds[kv.binding] = existing
      saveState(statePath, state)
      log(`kv ${kv.binding}: adopted existing ${existing} ("${title}")`)
      continue
    }

    log(`kv ${kv.binding}: creating namespace "${title}"`)
    const res = runWrangler(['kv', 'namespace', 'create', title], { cwd })
    if (res.status !== 0) {
      fail(`failed to create KV namespace "${title}":\n${combined(res)}`)
    }
    const m = combined(res).match(KV_ID)
    const id = m?.[1] ?? m?.[2]
    if (!id) {
      fail(`could not parse a KV namespace id from wrangler output:\n${combined(res)}`)
    }
    state.kvIds[kv.binding] = id
    saveState(statePath, state)
    log(`kv ${kv.binding}: created ${id}`)
  }
}

/**
 * Provision D1 databases idempotently: reuse ids from state, else adopt an
 * existing same-named database, else create one via `wrangler d1 create
 * <databaseName>` and parse the new UUID. State is saved after each resolve.
 *
 * NOTE: migrations are intentionally NOT applied here — they run AFTER the Worker
 * is deployed (see applyD1Migrations), because `d1 migrations apply` needs the
 * rendered wrangler.toml (the binding + database_id) to exist.
 */
function provisionD1(
  config: DeployConfig,
  state: DeployState,
  statePath: string,
  cwd: string,
): void {
  for (const d1 of config.bindings?.d1 ?? []) {
    if (state.d1Ids[d1.binding]) {
      log(`d1 ${d1.binding}: reuse ${state.d1Ids[d1.binding]}`)
      continue
    }

    const existing = findExistingD1Id(d1.databaseName, cwd)
    if (existing) {
      state.d1Ids[d1.binding] = existing
      saveState(statePath, state)
      log(`d1 ${d1.binding}: adopted existing ${existing} ("${d1.databaseName}")`)
      continue
    }

    log(`d1 ${d1.binding}: creating database "${d1.databaseName}"`)
    const res = runWrangler(['d1', 'create', d1.databaseName], { cwd })
    if (res.status !== 0) {
      fail(`failed to create D1 database "${d1.databaseName}":\n${combined(res)}`)
    }
    const m = combined(res).match(D1_ID)
    const id = m?.[1] ?? m?.[2]
    if (!id) {
      fail(`could not parse a D1 database id from wrangler output:\n${combined(res)}`)
    }
    state.d1Ids[d1.binding] = id
    saveState(statePath, state)
    log(`d1 ${d1.binding}: created ${id}`)
  }
}

/**
 * Apply remote D1 migrations for every D1 binding that declares a `migrationsDir`.
 * Best-effort: logs but does not fail the deploy on a nonzero exit. MUST run after
 * `wrangler deploy` — it relies on the rendered wrangler.toml to resolve the
 * binding's `database_id` and `migrations_dir`.
 */
function applyD1Migrations(config: DeployConfig, cwd: string): void {
  for (const d1 of config.bindings?.d1 ?? []) {
    if (!d1.migrationsDir) continue
    log(`d1 ${d1.binding}: applying remote migrations`)
    const mig = runWrangler(['d1', 'migrations', 'apply', d1.databaseName, '--remote'], { cwd })
    if (mig.status !== 0) {
      log(
        `warning: d1 migrations apply for "${d1.databaseName}" exited ${mig.status} — continuing:\n${combined(mig)}`,
      )
    }
  }
}

