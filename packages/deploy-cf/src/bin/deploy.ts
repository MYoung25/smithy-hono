/**
 * Discover + load the deploy config from the config dir. Honors `--config`,
 * else tries `smithy-deploy.config.mjs` then `.json`. `.mjs` is imported (its
 * `default` export taken); `.json` is read + parsed. Exits 1 with an actionable
 * message if nothing is found.
 */
async function loadConfig(configDir: string, override: string | undefined): Promise<DeployConfig> {
  const candidates = override
    ? [isAbsolute(override) ? override : resolve(configDir, override)]
    : [
        resolve(configDir, 'smithy-deploy.config.mjs'),
        resolve(configDir, 'smithy-deploy.config.json'),
      ]

  for (const abs of candidates) {
    if (!existsSync(abs)) continue
    if (abs.endsWith('.json')) {
      const raw = readFileSync(abs, 'utf8')
      log(`loaded config: ${abs}`)
      return JSON.parse(raw) as DeployConfig
    }
    // .mjs (or any importable module): take the default export.
    const mod = (await import(pathToFileURL(abs).href)) as { default?: DeployConfig }
    if (!mod.default) {
      fail(`config "${abs}" has no default export — export your defineDeployConfig(...) as default.`)
    }
    log(`loaded config: ${abs}`)
    return mod.default
  }

  fail(
    `no deploy config found (looked for ${candidates.join(', ')}). ` +
      `Create smithy-deploy.config.mjs exporting defineDeployConfig({ ... }) as default ` +
      `(from '@smithy-hono/deploy-cf'), or pass --config <path>.`,
  )
}

/**
 * Load the gitignored secrets file IF any secret declares `from: 'secretsFile'`.
 * Returns `{}` when no secret needs it (file absence is then fine). Exits 1 when
 * a secret needs it but the file is missing.
 */
function loadSecretsFile(config: DeployConfig, configDir: string): Record<string, string> {
  const needsFile = (config.secrets ?? []).some((s) => 'from' in s && s.from === 'secretsFile')
  const path = resolve(configDir, config.secretsFile ?? 'deploy.secrets.json')
  if (!needsFile) return {}
  if (!existsSync(path)) {
    fail(
      `secrets file not found at ${path} but a secret is declared { from: 'secretsFile' }. ` +
        `Create it as a JSON object keyed by secret name (and gitignore it).`,
    )
  }
  return JSON.parse(readFileSync(path, 'utf8')) as Record<string, string>
}

// ---------------------------------------------------------------------------
// wrangler runner
// ---------------------------------------------------------------------------

interface RunResult {
  status: number
  /**
   * Child stdout ONLY (capture mode); empty in inherit mode. Parse paths
   * (`parseJsonArray`) read this alone — wrangler writes JSON payloads to stdout and
   * warnings/banners to stderr, so keeping them separate stops a trailing stderr
   * line containing `]` from corrupting the greedy JSON slice (DEPLOY-CF-PKG-5).
   */
  stdout: string
  /** Child stderr ONLY (capture mode); empty in inherit mode. */
  stderr: string
}

/** stdout+stderr concatenated — for human-facing logs + error messages only. */
function combined(res: RunResult): string {
  return res.stdout + res.stderr
}

/**
 * Run `wrangler` via the consumer's local install (`npx wrangler ...`). In the
 * default capture mode stdout and stderr are captured SEPARATELY (parse paths use
 * stdout only); `inherit` streams the child's IO straight through (used for
 * build/deploy). `input` is piped to the child's stdin (used to feed secret values
 * to `secret put`).
 */
function runWrangler(
  args: string[],
  opts: { input?: string; cwd?: string; inherit?: boolean } = {},
): RunResult {
  const res = spawnSync('npx', ['wrangler', ...args], {
    cwd: opts.cwd,
    encoding: 'utf8',
    input: opts.input,
    stdio: opts.inherit ? 'inherit' : ['pipe', 'pipe', 'pipe'],
    env: process.env,
  })
  const status = res.status ?? (res.error ? 1 : 0)
  const stdout = opts.inherit ? '' : (res.stdout ?? '')
  const stderr = opts.inherit ? '' : (res.stderr ?? '')
  return { status, stdout, stderr }
}

