/** Run the assets build command (`sh -c`) in the config dir, inheriting IO. Exits 1 on failure. */
function buildAssets(cmd: string, cwd: string): void {
  log(`build: ${cmd}`)
  const res = spawnSync('sh', ['-c', cmd], { cwd, stdio: 'inherit', env: process.env })
  const status = res.status ?? (res.error ? 1 : 0)
  if (status !== 0) {
    fail(`assets build failed (exit ${status}): ${cmd}`)
  }
}

/**
 * Poll `https://<domain><apiPrefix>/healthz` up to ~10 times. A 200 ends early;
 * giving up only WARNS (cert issuance can lag — never fail the deploy on the probe).
 */
async function verifyHealth(domain: string, apiPrefix: string): Promise<void> {
  const url = `https://${domain}${apiPrefix}/healthz`
  log(`verify: probing ${url}`)
  for (let attempt = 1; attempt <= 10; attempt++) {
    try {
      const resp = await fetch(url)
      if (resp.status === 200) {
        log(`verify: healthy (200) on attempt ${attempt}`)
        return
      }
      log(`verify: attempt ${attempt} got ${resp.status}`)
    } catch {
      log(`verify: attempt ${attempt} failed (not reachable yet)`)
    }
    if (attempt < 10) await sleep(3000)
  }
  log('warning: /healthz never returned 200 — the deploy may still be fine (TLS/DNS can lag).')
}

// ---------------------------------------------------------------------------
// main
// ---------------------------------------------------------------------------

async function main(): Promise<void> {
  const args = parseArgs(process.argv.slice(2))
  const configDir = process.cwd()
  const { domain } = args

  // 2. Load config.
  const config = await loadConfig(configDir, args.configPath)

  // 3. Resolve api prefix + load the secrets file (if needed).
  const apiPrefix = apiPrefixOf(config)
  const fileValues = loadSecretsFile(config, configDir)

  // 4. Preflight wrangler + account.
  const accountId = ensureAccount(configDir)

  // 5. Best-effort zone precheck.
  await checkZone(domain)

  // 6. State file.
  const slug = domainSlug(domain)
  const { state, path: statePath } = loadState(configDir, slug)

  // 7. Provision bindings (idempotent; create/adopt only — no secrets/migrations
  //    yet, since both need the deployed Worker + rendered wrangler.toml).
  provisionKv(config, state, statePath, slug, configDir)
  provisionD1(config, state, statePath, configDir)

  // 8. Compute vars.
  const vars = computeVars(config, domain, apiPrefix)

  // 9. Render wrangler.toml.
  const toml = renderWrangler(config, {
    accountId,
    domain,
    kvIds: state.kvIds,
    d1Ids: state.d1Ids,
    vars,
  })
  const tomlPath = resolve(configDir, 'wrangler.toml')
  assertOverwritable(tomlPath, args.overwrite)
  writeFileSync(tomlPath, toml)
  log(`rendered: ${tomlPath}`)

  // 10. Build assets.
  if (config.assets?.buildCommand && !args.skipBuild) {
    buildAssets(config.assets.buildCommand, configDir)
  } else if (args.skipBuild) {
    log('build: skipped (--skip-build)')
  }

  // 11. Deploy. This CREATES the Worker — secrets + D1 migrations below depend on
  //     it existing (Wrangler v4 no longer auto-creates a script on `secret put`).
  log('deploy: wrangler deploy')
  const deploy = runWrangler(['deploy'], { cwd: configDir, inherit: true })
  if (deploy.status !== 0) {
    fail(`wrangler deploy failed (exit ${deploy.status}).`, deploy.status || 1)
  }

  // 12. Secrets — bound to the now-deployed Worker. Safe to set post-deploy: the
  //     Worker reads them at request time, not at module-eval, so there is no
  //     crash window and no re-deploy is needed.
  putSecrets(config, state, statePath, fileValues, args.rotateKeys, configDir)

  // 13. D1 migrations — now that wrangler.toml resolves the binding + database_id.
  applyD1Migrations(config, configDir)

  // 14. Verify + report.
  await verifyHealth(domain, apiPrefix)

  // Machine-relevant final output → stdout.
  process.stdout.write(`https://${domain}\n`)
  if (config.oidc) {
    process.stdout.write(
      `OIDC redirect URI to register at your IdP: https://${domain}${apiPrefix}/auth/callback\n`,
    )
  }
}

/**
 * Only run the orchestration when invoked as the CLI entrypoint (bin), NOT when
 * imported by a test/consumer. `realpathSync` resolves the npm bin symlink to the
 * real module path so the comparison holds when installed as a package bin.
 */
function isCliEntrypoint(): boolean {
  const invoked = process.argv[1]
  if (!invoked) return false
  try {
    return pathToFileURL(realpathSync(invoked)).href === import.meta.url
  } catch {
    return false
  }
}

if (isCliEntrypoint()) {
  main().catch((err: unknown) => {
    process.stderr.write(`error: ${err instanceof Error ? err.message : String(err)}\n`)
    process.exit(1)
  })
}
