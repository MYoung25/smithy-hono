#!/usr/bin/env node
/**
 * `smithy-hono-deploy` — the one-command Cloudflare Workers deploy orchestrator.
 *
 * Run from a consuming smithy-hono project directory (the config dir =
 * `process.cwd()`). Given a bare domain it: loads the deploy config, preflights
 * wrangler + the account, best-effort verifies the zone is active, provisions
 * the declared bindings (KV / D1; Durable Objects come from the rendered
 * migration), renders `wrangler.toml`, builds the UI, deploys, then syncs secrets
 * and applies D1 migrations against the deployed Worker, and finally probes
 * `/healthz` and reports the live URL (+ OIDC redirect URI).
 *
 * Ordering matters: secrets (`wrangler secret put`) and D1 migrations
 * (`wrangler d1 migrations apply`) run AFTER `wrangler deploy`, because both need
 * the Worker to exist and the rendered `wrangler.toml` to resolve bindings.
 * Wrangler v4 (unlike v3) does not auto-create a script on `secret put`.
 *
 * The whole flow is idempotent and ordered: provisioned ids + which secrets have
 * been put are persisted to `.smithy-deploy/<domainSlug>.json` so re-runs reuse
 * resources instead of recreating them. Secret VALUES are never logged and never
 * persisted — only a per-name "provisioned" boolean.
 *
 * Node APIs (argv, fs, child_process, fetch) are intentionally confined to this
 * CLI file; the importable library surface (config/wrangler/secrets) stays pure.
 *
 * Usage:
 *   smithy-hono-deploy <domain> [--rotate-keys] [--skip-build] [--config <path>]
 */

