/**
 * Compute the `[vars]` map: OIDC facts (if present) overlaid by the config's
 * `vars(ctx)` function. Returns a flat string→string record.
 */
function computeVars(config: DeployConfig, domain: string, apiPrefix: string): Record<string, string> {
  const vars: Record<string, string> = {}
  if (config.oidc) {
    vars.OIDC_ISSUER = config.oidc.issuer
    vars.OIDC_CLIENT_ID = config.oidc.clientId
    vars.OIDC_AUTHORIZE_URL = config.oidc.authorizeUrl
    vars.OIDC_TOKEN_URL = config.oidc.tokenUrl
  }
  const extra = config.vars?.({ domain, apiPrefix }) ?? {}
  return { ...vars, ...extra }
}

// ---------------------------------------------------------------------------
// wrangler.toml write guard
// ---------------------------------------------------------------------------

/** Banner the renderer stamps on every generated wrangler.toml (see wrangler.ts). */
const GENERATED_BANNER = '# Generated by @smithy-hono/deploy-cf'

/**
 * Refuse to clobber a HAND-AUTHORED wrangler.toml. The renderer emits only the
 * modeled subset (name/main/routes/assets/kv/DO/d1/vars/observability); a
 * pre-existing user file may carry [triggers]/[[queues]]/[[r2_buckets]]/[env.*]
 * etc. that would be silently dropped AND deployed on the same run. So if a
 * wrangler.toml exists and does NOT start with the generated banner, exit unless
 * `--overwrite` was passed (DEPLOY-CF-PKG-2).
 */
/**
 * Pure overwrite-safety decision (DEPLOY-CF-PKG-2), separated from the process
 * exit so it is unit-testable. Returns `{ ok: true }` when writing is safe (the
 * `--overwrite` flag was passed, no file exists, or the existing file carries the
 * generated banner), else `{ error }` describing why a hand-authored file must
 * not be clobbered. `readHead` is injected so tests need no filesystem.
 */
export function checkOverwritable(
  tomlPath: string,
  overwrite: boolean,
  exists: (p: string) => boolean = existsSync,
  readHead: (p: string) => string = (p) => readFileSync(p, 'utf8').slice(0, GENERATED_BANNER.length),
): { ok: true } | { error: string } {
  if (overwrite || !exists(tomlPath)) return { ok: true }
  if (readHead(tomlPath) === GENERATED_BANNER) return { ok: true }
  return {
    error:
      `refusing to overwrite ${tomlPath}: it was not generated by @smithy-hono/deploy-cf ` +
      `(no generated banner). This tool renders only the modeled subset, so a ` +
      `hand-authored wrangler.toml would lose any [triggers]/[[queues]]/[[r2_buckets]]/` +
      `[env.*]/[placement] sections. Move it aside, fold its settings into your ` +
      `smithy-deploy config, or re-run with --overwrite to replace it.`,
  }
}

function assertOverwritable(tomlPath: string, overwrite: boolean): void {
  const res = checkOverwritable(tomlPath, overwrite)
  if ('error' in res) fail(res.error)
}

