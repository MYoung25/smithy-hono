/**
 * List the secret NAMES actually bound to the deployed Worker via
 * `wrangler secret list` (JSON array of `{ name, type }`). Returns:
 *  - a `Set` of names when the listing is understood (possibly empty);
 *  - `undefined` when it can't be determined (nonzero exit or unparseable output),
 *    so the caller falls back to trusting persisted state rather than needlessly
 *    re-putting (which would REGENERATE generated secrets).
 */
function listWorkerSecrets(cwd: string): Set<string> | undefined {
  const res = runWrangler(['secret', 'list'], { cwd })
  if (res.status !== 0) return undefined
  const arr = parseJsonArray(res.stdout)
  // Distinguish a genuinely empty worker (`[]`) from an unparseable/empty capture:
  // only treat parse-empty as "no secrets" when stdout actually shows an empty array.
  if (arr.length === 0 && !/\[\s*\]/.test(res.stdout)) return undefined
  const names = arr
    .map((s) => s.name)
    .filter((n): n is string => typeof n === 'string')
  return new Set(names)
}

/**
 * Generate/sync secrets via `wrangler secret put <name>` (value piped over
 * stdin). A secret is re-put when EITHER `--rotate-keys` is set OR it is declared
 * but absent from the deployed Worker's live secret list — so a Worker recreated
 * (dashboard / account move / `wrangler delete`) while `.smithy-deploy/<slug>.json`
 * survives gets its secrets re-synced instead of silently starting with none
 * (DEPLOY-CF-PKG-4). A secret already provisioned AND present is skipped without
 * `--rotate-keys`, so `generate` secrets are not needlessly regenerated. Secret
 * VALUES are never logged and never persisted — only the per-name boolean in
 * state. State is saved after each successful put.
 */
function putSecrets(
  config: DeployConfig,
  state: DeployState,
  statePath: string,
  fileValues: Record<string, string>,
  rotateKeys: boolean,
  cwd: string,
): void {
  // Live secret names on the Worker (undefined = couldn't determine → trust state).
  const liveSecrets = listWorkerSecrets(cwd)

  for (const spec of config.secrets ?? []) {
    const markedProvisioned = state.secretsProvisioned[spec.name]
    // If we can read the Worker's secrets, "provisioned" additionally requires the
    // secret to actually be bound; otherwise fall back to the persisted boolean.
    const presentOnWorker = liveSecrets ? liveSecrets.has(spec.name) : markedProvisioned
    const effectivelyProvisioned = markedProvisioned && presentOnWorker

    if (effectivelyProvisioned && !rotateKeys) {
      log(`secret ${spec.name}: already provisioned (skip; use --rotate-keys to regen)`)
      continue
    }
    if (markedProvisioned && !presentOnWorker && !rotateKeys) {
      log(
        `secret ${spec.name}: state says provisioned but it is absent on the Worker — re-syncing`,
      )
    }
    const value = materializeSecret(spec, fileValues)
    log(`secret ${spec.name}: putting${rotateKeys ? ' (rotate)' : ''}`)
    const res = runWrangler(['secret', 'put', spec.name], { input: value, cwd })
    if (res.status !== 0) {
      // The value is fed over stdin, so wrangler's stdout/stderr never contains it
      // — safe to surface for diagnosis (e.g. "script not found", auth failures).
      fail(`failed to put secret "${spec.name}" (wrangler exited ${res.status}):\n${combined(res)}`)
    }
    state.secretsProvisioned[spec.name] = true
    saveState(statePath, state)
  }
}

