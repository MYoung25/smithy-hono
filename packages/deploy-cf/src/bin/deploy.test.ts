import { describe, it, expect } from 'vitest'
import { isValidDomain, domainSlug, resolveAccountId, parseJsonArray, checkOverwritable } from './deploy.js'

const GENERATED_BANNER = '# Generated by @smithy-hono/deploy-cf'

describe('checkOverwritable (DEPLOY-CF-PKG-2)', () => {
  it('allows writing when no wrangler.toml exists yet', () => {
    expect(checkOverwritable('/x/wrangler.toml', false, () => false)).toEqual({ ok: true })
  })

  it('allows overwriting a file this tool generated (carries the banner)', () => {
    const res = checkOverwritable('/x/wrangler.toml', false, () => true, () => GENERATED_BANNER)
    expect(res).toEqual({ ok: true })
  })

  it('refuses to clobber a hand-authored wrangler.toml (no banner)', () => {
    const res = checkOverwritable('/x/wrangler.toml', false, () => true, () => '# my hand-written config')
    expect('error' in res).toBe(true)
  })

  it('honors --overwrite even for a hand-authored file', () => {
    const res = checkOverwritable('/x/wrangler.toml', true, () => true, () => '# my hand-written config')
    expect(res).toEqual({ ok: true })
  })
})

describe('isValidDomain (DEPLOY-CF-PKG-3)', () => {
  it('accepts bare hostnames', () => {
    for (const d of ['app.example.com', 'a.b.example.com', 'a-b.example.com', 'x1.co', 'example.com']) {
      expect(isValidDomain(d), d).toBe(true)
    }
  })

  it('rejects underscores, scheme, path, whitespace, trailing/leading dashes and dots', () => {
    for (const d of [
      '',
      'a_b.example.com',
      'https://app.example.com',
      'app.example.com/path',
      'app .example.com',
      'app.example.com.',
      '-app.example.com',
      'app-.example.com',
      'app..example.com',
      '.example.com',
    ]) {
      expect(isValidDomain(d), d).toBe(false)
    }
  })

  it('rejects labels longer than 63 chars', () => {
    expect(isValidDomain(`${'a'.repeat(64)}.example.com`)).toBe(false)
    expect(isValidDomain(`${'a'.repeat(63)}.example.com`)).toBe(true)
  })
})

describe('domainSlug (DEPLOY-CF-PKG-3)', () => {
  it('is deterministic for a given domain', () => {
    expect(domainSlug('app.example.com')).toBe(domainSlug('app.example.com'))
  })

  it('does NOT collide distinct hostnames that share a readable slug', () => {
    const a = domainSlug('a.b.example.com')
    const b = domainSlug('a-b.example.com')
    expect(a).not.toBe(b)
  })

  it('is case-insensitive on the input', () => {
    expect(domainSlug('APP.Example.COM')).toBe(domainSlug('app.example.com'))
  })

  it('is filesystem-safe (only [a-z0-9-])', () => {
    expect(domainSlug('a.b.example.com')).toMatch(/^[a-z0-9-]+$/)
  })
})

describe('resolveAccountId (DEPLOY-CF-PKG-1)', () => {
  const HEX = 'a'.repeat(32)
  const HEX2 = 'b'.repeat(32)

  it('accepts an anchored 32-hex env override (lowercased)', () => {
    expect(resolveAccountId(HEX.toUpperCase(), '')).toEqual({ id: HEX })
  })

  it('rejects a malformed env override instead of trusting it', () => {
    const r = resolveAccountId(`junk-${HEX}`, `line ${HEX}`)
    expect('error' in r).toBe(true)
  })

  it('resolves the single id from whoami output', () => {
    const out = `Account Name  Account ID\nAcme  ${HEX}\n`
    expect(resolveAccountId(undefined, out)).toEqual({ id: HEX })
  })

  it('dedupes a repeated id to a single match', () => {
    const out = `${HEX}\ntoken tied to ${HEX}\n`
    expect(resolveAccountId(undefined, out)).toEqual({ id: HEX })
  })

  it('refuses to guess when whoami lists multiple accounts', () => {
    const out = `Acme ${HEX}\nBeta ${HEX2}\n`
    const r = resolveAccountId(undefined, out)
    expect('error' in r).toBe(true)
  })

  it('errors when no account id is present', () => {
    const r = resolveAccountId(undefined, 'not logged in')
    expect('error' in r).toBe(true)
  })
})

describe('parseJsonArray (DEPLOY-CF-PKG-5)', () => {
  it('parses a clean JSON array from stdout', () => {
    const arr = parseJsonArray('[{"title":"kv","id":"x"}]')
    expect(arr).toEqual([{ title: 'kv', id: 'x' }])
  })

  it('tolerates a leading npm/banner line on stdout', () => {
    const arr = parseJsonArray('npm notice\n[{"name":"db","uuid":"u"}]\n')
    expect(arr).toEqual([{ name: 'db', uuid: 'u' }])
  })

  it('returns [] on non-array / garbage', () => {
    expect(parseJsonArray('no json here')).toEqual([])
    expect(parseJsonArray('{"not":"array"}')).toEqual([])
  })

  it('parses correctly now that stderr warnings are NOT concatenated', () => {
    // stdout carries only the JSON payload; the stderr WARNING line that used to be
    // appended (and whose trailing `]` broke the greedy slice) is excluded upstream.
    const stdout = '[{"title":"kv","id":"abc"}]\n'
    expect(parseJsonArray(stdout)).toEqual([{ title: 'kv', id: 'abc' }])
  })
})
